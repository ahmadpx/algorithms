/**
 * queen attack
 * is a chess board having one queen can attack all surrounding squares
 * from its position to all directions and diagonal squares except obstacles
 * @param {Number} n chessboard squares number row/col
 * @param {Number} k number of obstacles
 * @param {Number} queenRow queen row position
 * @param {Number} queenCol queen col position
 * @param {Array<Array<Number>>} obstacles
 * @return {Number} number of squares a queen can attack
 */
function queensAttack(n, k, queenRow, queenCol, obstacles = []) {
  const isUpLeftSquare = (row, col) => queenRow - row === queenCol - col;
  const isUpRightSquare = (row, col) => queenRow - row === col - queenCol;
  const isDownRightSquare = (row, col) => row - queenRow === col - queenCol;
  const isDownLeftSquare = (row, col) => row - queenRow === queenCol - col;

  let attackedSquares = 0;

  const obstaclesMap = obstacles.reduce((acc, [row, col]) => {
    acc[`${row}-${col}`] = true;
    return acc;
  }, {});
  const isObstacle = (row, col) => !!obstaclesMap[`${row}-${col}`];

  /**
   * cross scenarios
   */
  // right
  for (let col = queenCol + 1; col <= n; col++) {
    if (isObstacle(queenRow, col)) break;
    ++attackedSquares;
  }
  // left
  for (let col = queenCol - 1; col > 0; col--) {
    if (isObstacle(queenRow, col)) break;
    ++attackedSquares;
  }
  // down
  for (let row = queenRow + 1; row <= n; row++) {
    if (isObstacle(row, queenCol)) break;
    ++attackedSquares;
  }
  // up
  for (let row = queenRow - 1; row > 0; row--) {
    if (isObstacle(row, queenCol)) break;
    ++attackedSquares;
  }

  /**
   * diagonal scenarios
   */
  // right down
  for (let row = queenRow + 1, lastCol = queenCol; row <= n; row++) {
    let shouldBreak = false;
    for (let col = lastCol + 1; col <= n; col++ && lastCol++) {
      if (isObstacle(row, col) && isDownRightSquare(row, col)) {
        shouldBreak = true;
        break;
      }
      if (isDownRightSquare(row, col)) {
        ++attackedSquares;
        break;
      }
    }
    if (shouldBreak) break;
  }
  // left down
  for (let row = queenRow + 1, lastCol = queenCol; row <= n; row++) {
    let shouldBreak = false;
    for (let col = lastCol - 1; col > 0; col-- && lastCol--) {
      if (isObstacle(row, col) && isDownLeftSquare(row, col)) {
        shouldBreak = true;
        break;
      }
      if (isDownLeftSquare(row, col)) {
        ++attackedSquares;
        break;
      }
    }
    if (shouldBreak) break;
  }
  // right up
  for (let row = queenRow - 1, lastCol = queenCol; row > 0; row--) {
    let shouldBreak = false;
    for (let col = lastCol + 1; col <= n; col++ && lastCol++) {
      if (isObstacle(row, col) && isUpRightSquare(row, col)) {
        shouldBreak = true;
        break;
      }
      if (isUpRightSquare(row, col)) {
        ++attackedSquares;
        break;
      }
    }
    if (shouldBreak) break;
  }
  // left up
  for (let row = queenRow - 1, lastCol = queenCol; row > 0; row--) {
    let shouldBreak = false;
    for (let col = lastCol - 1; col > 0; col-- && lastCol--) {
      if (isObstacle(row, col) && isUpLeftSquare(row, col)) {
        shouldBreak = true;
        break;
      }
      if (isUpLeftSquare(row, col)) {
        ++attackedSquares;
        break;
      }
    }
    if (shouldBreak) break;
  }

  return attackedSquares;
}

